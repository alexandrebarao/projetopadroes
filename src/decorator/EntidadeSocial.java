package decorator;

/**
 *
 * @author alexandrebarao
 */
public class EntidadeSocial {
    private String nome;
    private String id;
    
    public EntidadeSocial(String nome, String id) {
        this.nome = nome;
        this.id = id;
    }

    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }

    /**
     * @return the id
     */
    public String getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(String id) {
        this.id = id;
    }
    
    
}
